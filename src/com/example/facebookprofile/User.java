package com.example.facebookprofile;

public class User {
	String name;
	String username;
	String id;
	String gender;
	String link;
	
	User(String name, String username , String id , String gender , String link){
		this.name = name;
		this.username = username;
		this.id = id;
		this.gender = gender;
		this.link = link;
	}
}
